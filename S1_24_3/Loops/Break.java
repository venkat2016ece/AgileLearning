package S1_24_3.Loops;

public class Break {

	public static void main(String[] args) {
	 int x=0;
	 switch(x){
	 case 0:
		 System.out.println(0);
	 case 1:
		 System.out.println(1);
	 case 2:
		 System.out.println(2);
		 break;
	 case 3:
		 System.out.println(3);
		 default:
			 System.out.println("default");
	 }

	}

}
